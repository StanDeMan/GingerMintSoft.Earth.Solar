using GingerMintSoft.Earth.Location.Solar;
using GingerMintSoft.Earth.Location.Solar.Generator;

namespace GingerMintSoft.Earth.Location.Cmd
{
    public static class Program
    {
        private static void Main()
        {
            // Solar power plant location
            var powerPlant = new PowerPlant(
                "Eichstädt PV Anlage",
                232,                    // Höhe über NN in Metern
                48.1051268096319,       // Breitengrad in Dezimalgrad
                7.9085366169705145      // Längengrad in Dezimalgrad
            );

            powerPlant.Roofs.Add(new Roof()
            {
                Name = "Ostdach",
                Azimuth = Roof.CompassPoint.East,
                AzimuthDeviation = 15.0,
                Tilt = 45.0,
                Panels = new Panels()
                {
                    Panel =
                    [
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        }
                    ],
                }
            });

            powerPlant.Roofs.Add(new Roof()
            {
                Name = "Westdach",
                Azimuth = Roof.CompassPoint.West,
                AzimuthDeviation = 15.0,
                Tilt = 45.0,
                Panels = new Panels()
                {
                    Panel =
                    [
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        },
                        new Panel()
                        {
                            Name = "Ying Ping 420Wp",
                            Area = 1.78,
                            Efficiency = 0.21
                        }
                    ],
                }
            });

            var date = new DateTime(2024, 6, 21, 0, 0, 0, DateTimeKind.Utc);

            // Calculate solar radiation from sunrise to sunset
            Console.WriteLine($"Minütliche Solarstrahlung für {date.ToShortDateString()}");
            Console.WriteLine("Stunde:Minute\tStrahlung [W/m²]");

            var solarRadiationFromSunRiseTillSunSet = powerPlant.Calculate.Radiation(date);

            foreach (var solarMinutelyRadiation in solarRadiationFromSunRiseTillSunSet)
            {
                Console.WriteLine($"{solarMinutelyRadiation.Key:HH:mm}\t{solarMinutelyRadiation.Value:F2}");
            }

            Console.WriteLine("---------------");

            var roofs = powerPlant.Calculate.RadiationOnTiltedPanel(date);

            foreach (var roof in roofs)
            {
                Console.WriteLine($"Plant name: {roof.Name}");
                Console.WriteLine($"Roof tilt: {roof.Tilt}");
                Console.WriteLine($"Alignment of the system's roof generator: {roof.Azimuth + roof.AzimuthDeviation}");

                foreach (var data in roof.Radiation!)
                {
                    Console.WriteLine($"{data.Key:HH:mm}\t{data.Value:F2}");
                }

                Console.WriteLine("---------------Roof Generator Data");

                foreach (var data in roof.Earning())
                {
                    Console.WriteLine($"{data.Key:HH:mm}\t{data.Value:F2}");
                }

                Console.WriteLine("---------------");
            }

            Console.WriteLine("---------------Total Roof Generator power");
            var totalPower = powerPlant.MaximumTotalPower();

            foreach(var power in totalPower)
            {
                Console.WriteLine($"{power.Key:HH:mm}\t{power.Value:F2}");
            }

            Console.WriteLine("---------------Total Roof Generator energy");
            var totalEnergy = powerPlant.MaximumTotalEnergy();
            
            foreach (var energy in totalEnergy)
            {
                Console.WriteLine($"{energy.Key:HH:mm}\t{energy.Value:F4}");
            }

            Console.ReadKey();
