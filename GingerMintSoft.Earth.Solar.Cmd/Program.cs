using GingerMintSoft.Earth.Location.Solar;

namespace GingerMintSoft.Earth.Location.Cmd
{
    public static class Program
    {
        private static void Main()
        {
            // Solar power plant location
            var powerPlant = new PowerPlant(
                "Eichstädt PV Anlage",
                232,                    // Höhe über NN in Metern
                48.1051268096319,       // Breitengrad in Dezimalgrad
                7.9085366169705145      // Längengrad in Dezimalgrad
            );

            powerPlant.Roofs.Add(new Roof()
            {
                Name = "Ostdach",
                Azimuth = Roof.CompassPoint.East,
                AzimuthDeviation = 10.0,
                Tilt = 45.0
            });

            powerPlant.Roofs.Add(new Roof()
            {
                Name = "Westdach",
                Azimuth = Roof.CompassPoint.West,
                AzimuthDeviation = 10.0,
                Tilt = 45.0
            });

            var date = new DateTime(2024, 6, 21, 0, 0, 0, DateTimeKind.Utc);

            // Calculate solar radiation from sunrise to sunset
            Console.WriteLine($"Minütliche Solarstrahlung für {date.ToShortDateString()}");
            Console.WriteLine("Stunde:Minute\tStrahlung [W/m²]");

            var solarDailyRadiationFromSunRiseTillSunSet = powerPlant.Calculate.Radiation(date);

            foreach (var solarMinutelyRadiation in solarDailyRadiationFromSunRiseTillSunSet)
            {
                Console.WriteLine($"{solarMinutelyRadiation.Key:HH:mm}\t{solarMinutelyRadiation.Value:F2}");
            }

            Console.WriteLine("---------------");

            var roofs = powerPlant.Calculate.RadiationOnTiltedPanel(date);

            foreach (var roof in roofs)
            {
                Console.WriteLine($"Plant name: {roof.Name}");
                Console.WriteLine($"Roof tilt: {roof.Tilt}");
                Console.WriteLine($"Alignment of the system's roof generator: {roof.Azimuth + roof.AzimuthDeviation}");

                foreach (var data in roof.Earning!)
                {
                    Console.WriteLine($"{data.Key:HH:mm}\t{data.Value:F2}");
                }

                Console.WriteLine("---------------");
            }

            Console.ReadKey();
        }
    }
